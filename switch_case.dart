void main() {
  String grade = 'A';

//switch - applicable only for int and string

  switch (grade) {
    case 'A':
      print("Excellent");
      break;
    case 'B':
      print("Great");
      break;
    case 'C':
      print("Good");
      break;
    default:
      print("Invalid grade");
  }
}
